public class Medidas {
    private float peso;
    private float talla;

    public Medidas(float peso, float talla) {
        this.peso = peso;
        this.talla = talla;
    }

    public float getPeso() {
        return peso;
    }

    public void setPeso(float peso) {
        this.peso = peso;
    }

    public float getTalla() {
        return talla;
    }

    public void setTalla(float talla) {
        this.talla = talla;
    }
}
